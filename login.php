    $(document).ready(function () {
        // Xử lý sự kiện khi nhấn nút "Đăng nhập"
        $("#loginform").on("submit", function (event) {
            event.preventDefault(); // Ngăn hành động gửi form mặc định

            // Lấy giá trị từ input
            let playerID = $("input[name='idfaifai']").val();
            let loginButton = $("button[name='login']");

            // Kiểm tra nếu input rỗng
            if (!playerID) {
                $("#error-message").text("Vui lòng nhập ID người chơi.");
                return;
            }

            // Vô hiệu hóa nút và thay đổi nội dung nút
            loginButton.prop("disabled", true).text("Đang đăng nhập...");

            // Gửi dữ liệu qua AJAX
            $.ajax({
                url: "/checkid.php", // Đường dẫn xử lý form (thay bằng file PHP thực tế)
                method: "POST", // Phương thức HTTP
                data: { idfaifai: playerID }, // Dữ liệu gửi đi
                dataType: "json", // Định dạng dữ liệu trả về (JSON)
                success: function (response) {
                    if (response.status == true) {
                        // Xử lý thành công
                        window.location.href = "/m/loaithe.php";
                    } else {
                        // Hiển thị thông báo lỗi
                        $("#error-message").text(response.error_msg || "Đăng nhập thất bại.");
                        // Kích hoạt lại nút và khôi phục nội dung
                        loginButton.prop("disabled", false).text("Đăng nhập");
                    }
                },
                error: function () {
                    // Xử lý lỗi mạng hoặc server
                    $("#error-message").text("Có lỗi xảy ra. Vui lòng thử lại.");
                    // Kích hoạt lại nút và khôi phục nội dung
                    loginButton.prop("disabled", false).text("Đăng nhập");
                },
            });
        });
    });
</script>


